var stateMap = { "Confirmed" : "Accepted", "Standby" : "Accepted" };

function prepareDb(db, keySelector, itemSelector)
{
	var retval = {};
	
	for(var i = 0, T; T = db[i]; i++)
	{
		var key = keySelector(T);
		var list = retval[key] || (retval[key] = []);
		list.push(itemSelector ? itemSelector(T) : T);
	}
	
	return retval;
}

